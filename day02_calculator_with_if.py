opr = input("enter any operator")
num1 = float(input("enter num1"))
num2 = float(input("enter num2"))
if opr == "+":
    print(num1+num2)
elif opr == "-":
    print(num1-num2)  
elif opr == "*":
    print(num1*num2)  
elif opr == "/":
    print(num1/num2)          
elif opr == "%":
    print(num1%num2)      
else:
    print("enter any value")    
