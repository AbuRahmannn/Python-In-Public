#string indexing
#[start : end : step]


num = "1234-5678-9012-3456"

print("print the starting index of string")
print(num[0])

print("print end index of the string")
print(num[ :4])

print("print end of the string")
print(num[5:9])

print("print end string starting from 5")
print(num[5: ])

print("print end of the string")
print(num[-1])

print("print number by spacing 3 nums")
print(num[: : 3])